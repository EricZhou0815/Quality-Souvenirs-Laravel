    public function changeStatus($id){
        $user=User::find($id);
        if($user->enabled=='enabled'){
            $user->enabled='disabled';
        }
        elseif($user->enabled=='disabled'){
            $user->enabled='enabled';
        }
        $user->save();
    
        return Redirect('users');

    }


